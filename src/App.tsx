// ── SCORE MANAGEMENT VIEW (with auto‑create + autosave) ────────────────────
function ScoreManagementView({
  school,
  klass,
  students,
  asmts,
  onUpdateScore,
  onBack,
  onBackSchool,
  onRefresh
}: {
  school: any;
  klass: any;
  students: any[];
  asmts: any[];
  onUpdateScore: (id: string, score: number) => Promise<void>;
  onBack: () => void;
  onBackSchool: () => void;
  onRefresh?: () => void;
}) {
  const [selectedTerm, setSelectedTerm] = useState<Term>(1);
  const [selectedType, setSelectedType] = useState<AType>("test1");
  const [editingScores, setEditingScores] = useState<Record<string, string>>({});
  const [saving, setSaving] = useState<Record<string, boolean>>({});
  const [loading, setLoading] = useState(false);
  const [saveSuccess, setSaveSuccess] = useState<Record<string, boolean>>({});
  const [debugInfo, setDebugInfo] = useState<string>("");
  const [generating, setGenerating] = useState(false);

  const maxScore = AMAX[selectedType];
  const debounceTimers = useRef<Record<string, NodeJS.Timeout>>({});

  // Load existing scores
  useEffect(() => {
    setLoading(true);
    const existingScores: Record<string, string> = {};
    let missingCount = 0;
    students.forEach((student: any) => {
      const assessment = asmts.find((a: any) =>
        a.student_id === student.id &&
        a.term === selectedTerm &&
        a.type === selectedType &&
        a.year === klass.academic_year
      );
      if (assessment) {
        existingScores[student.id] = String(assessment.score);
      } else {
        missingCount++;
        existingScores[student.id] = "";
      }
    });
    setEditingScores(existingScores);
    setLoading(false);
    if (missingCount > 0) {
      setDebugInfo(`⚠️ ${missingCount} student(s) missing assessments – will auto‑create on save.`);
    } else {
      setDebugInfo(`✅ All students have assessments for ${AL[selectedType]} (Term ${selectedTerm}).`);
    }
  }, [students, asmts, selectedTerm, selectedType, klass.academic_year]);

  // ── Ensure assessment exists (create if missing) ──────────────────────────
  const ensureAssessment = async (studentId: string): Promise<string | null> => {
    // Check if assessment already exists in the local asmts array
    let assessment = asmts.find((a: any) =>
      a.student_id === studentId &&
      a.term === selectedTerm &&
      a.type === selectedType &&
      a.year === klass.academic_year
    );
    if (assessment) return assessment.id;

    // If not, create it in Supabase
    const newAssessment = {
      id: uid(),
      student_id: studentId,
      term: selectedTerm,
      type: selectedType,
      score: 0,
      max_score: AMAX[selectedType],
      year: klass.academic_year,
      created_at: today()
    };

    const { error, data } = await supabase
      .from('assessments')
      .insert([newAssessment])
      .select();

    if (error) {
      console.error('Failed to create assessment:', error);
      return null;
    }

    // Update local asmts state (via parent) – we'll rely on onRefresh after saving
    // But we can also add it to the local list temporarily
    // For simplicity, we'll trigger a refresh after creation
    if (onRefresh) onRefresh();
    return newAssessment.id;
  };

  // ── Autosave function ─────────────────────────────────────────────────────
  const performSave = async (studentId: string, value: string) => {
    if (!value || value === "") return;
    const score = parseFloat(value);
    if (isNaN(score) || score < 0 || score > maxScore) return;

    setSaving(prev => ({ ...prev, [studentId]: true }));
    setSaveSuccess(prev => ({ ...prev, [studentId]: false }));

    try {
      // Ensure assessment exists
      let assessmentId = asmts.find((a: any) =>
        a.student_id === studentId &&
        a.term === selectedTerm &&
        a.type === selectedType &&
        a.year === klass.academic_year
      )?.id;

      if (!assessmentId) {
        // Create it
        const newId = await ensureAssessment(studentId);
        if (!newId) {
          throw new Error('Could not create assessment');
        }
        assessmentId = newId;
        // After creation, we need to refresh data to get the new assessment in the list
        // But we can still proceed to update the score
        // We'll call onUpdateScore with the new ID, but we need to ensure the parent state includes it.
        // Since we have onRefresh, we'll call it after save.
      }

      await onUpdateScore(assessmentId, Math.round(score));
      setSaveSuccess(prev => ({ ...prev, [studentId]: true }));
      setEditingScores(prev => ({ ...prev, [studentId]: String(Math.round(score)) }));

      // Refresh to get the updated assessment list
      if (onRefresh) onRefresh();
    } catch (err) {
      console.error('Autosave error:', err);
      setSaveSuccess(prev => ({ ...prev, [studentId]: false }));
    } finally {
      setSaving(prev => ({ ...prev, [studentId]: false }));
      setTimeout(() => {
        setSaveSuccess(prev => ({ ...prev, [studentId]: false }));
      }, 2000);
    }
  };

  // ── Debounced change handler ─────────────────────────────────────────────
  const handleScoreChange = (studentId: string, value: string) => {
    setEditingScores(prev => ({ ...prev, [studentId]: value }));
    setSaveSuccess(prev => ({ ...prev, [studentId]: false }));

    if (debounceTimers.current[studentId]) {
      clearTimeout(debounceTimers.current[studentId]);
    }

    if (!value || value === "") return;
    const num = parseFloat(value);
    if (isNaN(num) || num < 0 || num > maxScore) return;

    debounceTimers.current[studentId] = setTimeout(() => {
      performSave(studentId, value);
    }, 800);
  };

  // ── Manual save on blur ────────────────────────────────────────────────────
  const handleBlur = (studentId: string, value: string) => {
    if (debounceTimers.current[studentId]) {
      clearTimeout(debounceTimers.current[studentId]);
      delete debounceTimers.current[studentId];
    }
    if (value && value !== "") {
      const num = parseFloat(value);
      if (!isNaN(num) && num >= 0 && num <= maxScore) {
        performSave(studentId, value);
      }
    }
  };

  // ── Bulk generate (optional) ──────────────────────────────────────────────
  const generateMissingAssessments = async () => {
    if (generating) return;
    const missingStudents = students.filter((s: any) => {
      const assessment = asmts.find((a: any) =>
        a.student_id === s.id &&
        a.term === selectedTerm &&
        a.type === selectedType &&
        a.year === klass.academic_year
      );
      return !assessment;
    });

    if (missingStudents.length === 0) {
      alert('All students already have assessments.');
      return;
    }

    if (!confirm(`Create ${missingStudents.length} missing assessments?`)) return;

    setGenerating(true);
    try {
      const newAssessments = missingStudents.map((s: any) => ({
        id: uid(),
        student_id: s.id,
        term: selectedTerm,
        type: selectedType,
        score: 0,
        max_score: AMAX[selectedType],
        year: klass.academic_year,
        created_at: today()
      }));

      const batchSize = 100;
      for (let i = 0; i < newAssessments.length; i += batchSize) {
        const batch = newAssessments.slice(i, i + batchSize);
        const { error } = await supabase.from('assessments').insert(batch);
        if (error) throw error;
      }

      alert(`✅ Created ${newAssessments.length} assessments.`);
      if (onRefresh) onRefresh();
    } catch (err: any) {
      console.error('Error generating assessments:', err);
      alert(`❌ Failed: ${err.message}`);
    } finally {
      setGenerating(false);
    }
  };

  const handleSaveAll = async () => {
    const savePromises = students.map((student: any) => {
      const value = editingScores[student.id];
      if (value && value !== "") {
        const score = parseFloat(value);
        if (!isNaN(score) && score >= 0 && score <= maxScore) {
          const assessment = asmts.find((a: any) =>
            a.student_id === student.id &&
            a.term === selectedTerm &&
            a.type === selectedType &&
            a.year === klass.academic_year
          );
          if (assessment) {
            return onUpdateScore(assessment.id, Math.round(score));
          }
        }
      }
      return Promise.resolve();
    });
    await Promise.all(savePromises);
    if (onRefresh) onRefresh();
  };

  const getStudentScore = (studentId: string) => {
    return asmts.find((a: any) =>
      a.student_id === studentId &&
      a.term === selectedTerm &&
      a.type === selectedType &&
      a.year === klass.academic_year
    );
  };

  if (loading) {
    return (
      <div className="p-4 sm:p-6 lg:p-8 max-w-7xl mx-auto w-full">
        <div className="flex items-center justify-center py-24">
          <Loader2 size={48} className="animate-spin text-primary" />
        </div>
      </div>
    );
  }

  return (
    <div className="p-4 sm:p-6 lg:p-8 max-w-7xl mx-auto w-full">
      <nav className="flex items-center gap-2 text-sm text-muted-foreground mb-4 sm:mb-6 overflow-x-auto">
        <button onClick={onBackSchool} className="hover:text-foreground transition-colors whitespace-nowrap">Schools</button>
        <ChevronRight size={14} className="flex-shrink-0" />
        <button onClick={onBack} className="hover:text-foreground transition-colors whitespace-nowrap truncate max-w-[120px]">{school.name}</button>
        <ChevronRight size={14} className="flex-shrink-0" />
        <span className="text-foreground font-semibold truncate">{klass.name}</span>
      </nav>

      <div className="flex flex-col sm:flex-row items-start sm:items-center justify-between mb-6 gap-4">
        <div>
          <h1 className="text-2xl font-bold tracking-tight">Score Management</h1>
          <p className="text-sm text-muted-foreground">{klass.name} · {klass.subject} · {klass.academic_year}</p>
          <p className="text-xs text-muted-foreground mt-1">{debugInfo}</p>
          <p className="text-xs text-muted-foreground mt-0.5">
            📊 {asmts.length} assessments in DB · {students.length} students
          </p>
        </div>
        <div className="flex flex-wrap gap-2">
          <button
            onClick={generateMissingAssessments}
            disabled={generating}
            className="flex items-center gap-2 bg-amber-500 hover:bg-amber-600 text-white px-4 py-2.5 rounded-xl text-sm font-semibold transition-colors disabled:opacity-50"
          >
            {generating ? <Loader2 size={16} className="animate-spin" /> : <Plus size={16} />}
            {generating ? 'Creating...' : 'Bulk Create'}
          </button>
          {onRefresh && (
            <button
              onClick={onRefresh}
              className="flex items-center gap-2 bg-muted hover:bg-muted/70 px-4 py-2.5 rounded-xl text-sm font-semibold transition-colors"
            >
              <RefreshCw size={16} /> Reload
            </button>
          )}
          <button
            onClick={handleSaveAll}
            className="flex items-center gap-2 bg-primary text-primary-foreground px-5 py-2.5 rounded-xl text-sm font-semibold hover:opacity-90 transition-opacity shadow-sm"
          >
            <SaveIcon size={18} /> Save All
          </button>
        </div>
      </div>

      <div className="bg-card rounded-2xl border border-border p-4 sm:p-6 mb-6 shadow-sm">
        <div className="flex flex-wrap gap-4">
          <div>
            <label className={labelCls}>Term</label>
            <select
              value={selectedTerm}
              onChange={(e) => setSelectedTerm(Number(e.target.value) as Term)}
              className={inputCls + " w-40"}
            >
              <option value={1}>First Term</option>
              <option value={2}>Second Term</option>
              <option value={3}>Third Term</option>
            </select>
          </div>
          <div>
            <label className={labelCls}>Assessment Type</label>
            <select
              value={selectedType}
              onChange={(e) => setSelectedType(e.target.value as AType)}
              className={inputCls + " w-48"}
            >
              {ATYPES.map(t => <option key={t} value={t}>{AL[t]} (Max: {AMAX[t]})</option>)}
            </select>
          </div>
          <div className="flex items-end">
            <div className="text-sm text-muted-foreground">
              Max Score: <span className="font-bold text-foreground">{maxScore}</span>
            </div>
          </div>
        </div>
      </div>

      <div className="bg-card rounded-2xl border border-border overflow-hidden shadow-sm">
        <div className="overflow-x-auto">
          <table className="w-full text-sm">
            <thead>
              <tr className="border-b border-border bg-muted/30">
                <th className="text-left px-4 sm:px-6 py-4 text-xs font-bold text-muted-foreground uppercase tracking-wider">#</th>
                <th className="text-left px-4 sm:px-6 py-4 text-xs font-bold text-muted-foreground uppercase tracking-wider">Student</th>
                <th className="text-left px-4 sm:px-6 py-4 text-xs font-bold text-muted-foreground uppercase tracking-wider">Score</th>
                <th className="text-left px-4 sm:px-6 py-4 text-xs font-bold text-muted-foreground uppercase tracking-wider">Grade</th>
                <th className="text-left px-4 sm:px-6 py-4 text-xs font-bold text-muted-foreground uppercase tracking-wider">%</th>
                <th className="text-left px-4 sm:px-6 py-4 text-xs font-bold text-muted-foreground uppercase tracking-wider">Status</th>
              </tr>
            </thead>
            <tbody className="divide-y divide-border">
              {students.map((student: any, index: number) => {
                const assessment = getStudentScore(student.id);
                const currentScore = editingScores[student.id] || "";
                const scoreVal = parseFloat(currentScore || "0");
                const isValid = !isNaN(scoreVal) && scoreVal >= 0 && scoreVal <= maxScore;
                const grade = isValid ? gradeStr(scoreVal, maxScore) : "N/A";
                const percentage = isValid ? pct(scoreVal, maxScore) : 0;
                const isSaving = saving[student.id] || false;
                const isSuccess = saveSuccess[student.id] || false;

                return (
                  <tr key={student.id} className="hover:bg-muted/20 transition-colors">
                    <td className="px-4 sm:px-6 py-4 text-muted-foreground font-mono">{index + 1}</td>
                    <td className="px-4 sm:px-6 py-4">
                      <div className="flex items-center gap-3">
                        <Avatar student={student} size="sm" />
                        <span className="font-medium">{student.first_name} {student.last_name}</span>
                      </div>
                    </td>
                    <td className="px-4 sm:px-6 py-4">
                      <div className="flex items-center gap-2">
                        <input
                          type="number"
                          min={0}
                          max={maxScore}
                          value={currentScore}
                          onChange={(e) => handleScoreChange(student.id, e.target.value)}
                          onBlur={(e) => handleBlur(student.id, e.target.value)}
                          className="w-24 border border-border rounded-lg px-3 py-1.5 text-sm font-mono focus:outline-none focus:ring-2 focus:ring-primary/20"
                          placeholder="-"
                        />
                        <span className="text-xs text-muted-foreground">/ {maxScore}</span>
                      </div>
                    </td>
                    <td className="px-4 sm:px-6 py-4">
                      {currentScore && currentScore !== "" && isValid && (
                        <span className={`text-sm font-bold px-2.5 py-1 rounded-full ${GRADE_COLORS[grade as Grade] || "bg-gray-100 text-gray-600"}`}>
                          {grade}
                        </span>
                      )}
                    </td>
                    <td className="px-4 sm:px-6 py-4">
                      {currentScore && currentScore !== "" && isValid && (
                        <span className={`text-sm font-mono ${percentage >= 80 ? "text-emerald-600" : percentage >= 60 ? "text-amber-600" : "text-red-600"}`}>
                          {percentage}%
                        </span>
                      )}
                    </td>
                    <td className="px-4 sm:px-6 py-4">
                      <span className="text-xs flex items-center gap-1">
                        {isSaving ? <Loader2 size={12} className="animate-spin" /> : isSuccess ? <Check size={12} className="text-emerald-600" /> : null}
                        {isSaving ? 'Saving...' : isSuccess ? 'Saved' : ''}
                      </span>
                    </td>
                  </tr>
                );
              })}
            </tbody>
          </table>
        </div>
      </div>
    </div>
  );
}

